import mongoose from "mongoose";

const tbResSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  groupSize: {
    type: Number,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  time: {
    type: String,
    required: true,
  },
});

const tbRes = mongoose.models.reserve || mongoose.model("reserve", tbResSchema);

export default tbRes;